# Содержит функции для вычисления площади и периметра треугольника.

def area(a, h):
    """
    Вычисляет площадь треугольника.

    Аргументы:
        a (float): длина основания треугольника
        h (float): высота, проведённая к основанию

    Возвращает:
        float: площадь треугольника

    Пример:
        >>> area(6, 4)
        12
    """
    return a * h / 2


def perimeter(a, b, c):
    """
    Вычисляет периметр треугольника.

    Аргументы:
        a (float): первая сторона треугольника
        b (float): вторая сторона треугольника
        c (float): третья сторона треугольника

    Возвращает:
        float: периметр треугольника

    Пример:
        >>> perimeter(3, 4, 5)
        12
    """
    return a + b + c
    
    