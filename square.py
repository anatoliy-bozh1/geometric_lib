# Содержит функции для вычисления площади и периметра квадрата.

def area(a):
    """
    Вычисляет площадь квадрата.

    Аргументы:
        a (float): длина стороны квадрата

    Возвращает:
        float: площадь квадрата

    Пример:
        >>> area(5)
        25
    """
    return a * a


