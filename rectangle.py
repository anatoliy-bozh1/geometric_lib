# Содержит функции для вычисления площади и периметра прямоугольника.

def area(a, b):
    """
    Вычисляет площадь прямоугольника.

    Аргументы:
        a (float): длина прямоугольника
        b (float): ширина прямоугольника

    Возвращает:
        float: площадь прямоугольника

    Исключения:
        TypeError: если длина или ширина не являются числами
        ValueError: если длина или ширина отрицательные

    Пример:
        area(3, 4) -> 12
    """
    if not all(isinstance(x, (int, float)) for x in (a, b)):
        raise TypeError("Длина и ширина должны быть числами")
    
    if a < 0 or b < 0:
        raise ValueError("Длина и ширина должны быть положительными числами")
    
    return a * b


def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника.

    Аргументы:
        a (float): длина прямоугольника
        b (float): ширина прямоугольника

    Возвращает:
        float: периметр прямоугольника

    Исключения:
        TypeError: если длина или ширина не являются числами
        ValueError: если длина или ширина отрицательные

    Пример:
        perimeter(3, 4) -> 14
    """
    if not all(isinstance(x, (int, float)) for x in (a, b)):
        raise TypeError("Длина и ширина должны быть числами")
    
    if a < 0 or b < 0:
        raise ValueError("Длина и ширина должны быть положительными числами")
    
    return (a + b) * 2
    
    