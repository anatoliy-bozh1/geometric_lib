# Содержит функции для вычисления площади и длины окружности (периметра) круга.

import math


def area(r):
    """
    Вычисляет площадь круга.

    Аргументы:
        r (float): радиус круга

    Возвращает:
        float: площадь круга

    Пример:
        >>> area(3)
        28.27
    """
    return math.pi * r * r


def perimeter(r):
    """
    Вычисляет длину окружности (периметр круга).

    Аргументы:
        r (float): радиус круга

    Возвращает:
        float: длину окружности

    Пример:
        >>> perimeter(3)
        18.85
    """
    return 2 * math.pi * r
    
    